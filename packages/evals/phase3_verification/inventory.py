from __future__ import annotations

from typing import Literal

from packages.evals.phase3_verification.models import CapabilityInventoryItem


def _capability(
    capability_id: str,
    name: str,
    implementation: tuple[str, ...],
    configuration: tuple[str, ...],
    commands: tuple[str, ...],
    tests: tuple[str, ...],
    reports: tuple[str, ...],
    documentation: tuple[str, ...],
    *,
    dependencies: tuple[str, ...] = (),
    default_state: Literal["enabled", "disabled", "conditional"] = "enabled",
    external_requirement: Literal["none", "optional", "local_postgres"] = "none",
    limitations: tuple[str, ...],
) -> CapabilityInventoryItem:
    return CapabilityInventoryItem(
        capability_id=capability_id,
        name=name,
        implementation_locations=implementation,
        configuration=configuration,
        cli_commands=commands,
        tests=tests,
        generated_reports=reports,
        documentation=documentation,
        optional_dependencies=dependencies,
        default_state=default_state,
        external_service_requirement=external_requirement,
        known_limitations=limitations,
    )


